# using numpy 
import numpy as np

# create a 1D NumPy array 
arr = np.array([1, 2, 3, 4, 5])

# compute the sum of all elements 
array_sum = arr.sum()
print("Sum of elements:", array_sum) 

# Find the mean and standard deviation 
array_mean = arr.mean()
array_std = arr.std()
print("the mean of the array is Mean:", array_mean)
print("The standard deviation of the array is", array_std)
 # multiply eaach element by 2
array_multiplied = arr * 2
print("Array multiplied by 2:", array_multiplied)













